module EightToOneMUX( input x1,x2,x3,x4,x5,x6,x7,x8, s1, s2, s3, output y);
wire w1, w2 , w3, w4, w5;
FourtoOneMUX gate1 (.x1(x1),.x2(x2),.x3(x3),.x4(x4),.s1(s1),.s2(s2),.y(w1));
FourtoOneMUX gate2 (.x1(x5),.x2(x6),.x3(x7),.x4(x8),.s1(s1),.s2(s2),.y(w2));
and u1(w3, w2, s3); 
not u2(w4, s3); 
and u3(w5, w1, w4); 
or u4(y, w3, w5); 
endmodule

